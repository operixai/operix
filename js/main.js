let lang = localStorage.getItem('operix_lang') || 'en';
function t(key) { return T[lang][key] || T['en'][key] || key; }

function applyLang() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.dataset.t;
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  const items = t('marquee_items');
  if (Array.isArray(items)) {
    document.querySelectorAll('.marquee-track').forEach(track => {
      const doubled = [...items, ...items];
      track.innerHTML = doubled.map(item =>
        `<span class="marquee-item"><span class="marquee-sep"></span>${item}</span>`
      ).join('');
    });
  }
  document.documentElement.lang = lang;
}

function setLang(l) {
  lang = l;
  localStorage.setItem('operix_lang', l);
  applyLang();
  haptic('medium');
}

// ── HAPTIC ──
function haptic(type = 'light') {
  if (!navigator.vibrate) return;
  const p = { light:10, medium:25, heavy:50, double:[20,60,20], success:[10,40,80] };
  navigator.vibrate(p[type] || 10);
}

function addHaptics() {
  document.querySelectorAll('.btn, button[type="submit"], .plan-btn').forEach(el => {
    el.addEventListener('pointerdown', () => haptic('light'));
    el.addEventListener('click', () => haptic('medium'));
  });
  document.querySelectorAll('nav a, .footer-col a').forEach(el => {
    el.addEventListener('pointerdown', () => haptic('light'));
  });
  document.querySelectorAll('.card, .plan, .contact-method, .case-card, .blog-card, .testimonial').forEach(el => {
    el.addEventListener('pointerdown', () => haptic('light'));
  });
  document.querySelectorAll('.faq-q').forEach(el => {
    el.addEventListener('click', () => haptic('medium'));
  });
  document.querySelectorAll('.lang-btn').forEach(el => {
    el.addEventListener('click', () => haptic('medium'));
  });
  document.querySelectorAll('form').forEach(form => {
    form.addEventListener('submit', () => haptic('success'));
  });
}

// ── ANIMATIONS ──
function initAnim() {
  setTimeout(function() {
    var obs = new IntersectionObserver(function(entries) {
      entries.forEach(function(e) {
        if (e.isIntersecting) {
          var delay = parseInt(e.target.dataset.delay || 0);
          setTimeout(function() { e.target.classList.add("in"); }, delay);
          obs.unobserve(e.target);
        }
      });
    }, { threshold: 0.05, rootMargin: "0px 0px -20px 0px" });
    document.querySelectorAll(".anim").forEach(function(el, i) {
      if (!el.dataset.delay) el.dataset.delay = (i % 6) * 60;
      obs.observe(el);
    });
  }, 150);
}

// ── NAV ──
function initNav() {
  const nav = document.querySelector('nav');
  if (!nav) return;
  window.addEventListener('scroll', () => {
    nav.classList.toggle('scrolled', window.scrollY > 40);
  }, { passive: true });
  const page = location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.nav-links a').forEach(a => {
    if (a.getAttribute('href') === page) a.classList.add('active');
  });
}

// ── FAQ ──
function initFAQ() {
  document.querySelectorAll('.faq-q').forEach(q => {
    q.addEventListener('click', () => {
      const item = q.closest('.faq-item');
      const wasOpen = item.classList.contains('open');
      document.querySelectorAll('.faq-item.open').forEach(i => i.classList.remove('open'));
      if (!wasOpen) item.classList.add('open');
    });
  });
}

// ── FORM ──
function initForm() {
  const form = document.querySelector('#contact-form');
  if (!form) return;
  form.addEventListener('submit', async e => {
    e.preventDefault();
    const btn = form.querySelector('button[type="submit"]');
    btn.textContent = lang === 'ru' ? 'Отправляем...' : 'Sending...';
    btn.disabled = true;
    haptic('success');
    await new Promise(r => setTimeout(r, 1200));
    btn.textContent = lang === 'ru' ? 'Отправлено! Скоро свяжемся.' : "Sent! We'll be in touch.";
    btn.style.background = '#2d7a3a';
    btn.style.color = '#fff';
    form.reset();
    setTimeout(() => {
      btn.textContent = t('contact_send');
      btn.style.background = '';
      btn.style.color = '';
      btn.disabled = false;
    }, 4000);
  });
}

// ── CURSOR GLOW ──
function initCursor() {
  if (window.matchMedia('(pointer: coarse)').matches) return;
  const g = document.createElement('div');
  g.style.cssText = 'position:fixed;pointer-events:none;z-index:9997;width:320px;height:320px;border-radius:50%;background:radial-gradient(circle,rgba(232,255,71,0.04) 0%,transparent 70%);transform:translate(-50%,-50%);will-change:transform;';
  document.body.appendChild(g);
  let mx=0,my=0,cx=0,cy=0;
  document.addEventListener('mousemove', e => { mx=e.clientX; my=e.clientY; });
  (function loop(){ cx+=(mx-cx)*0.08; cy+=(my-cy)*0.08; g.style.left=cx+'px'; g.style.top=cy+'px'; requestAnimationFrame(loop); })();
}

// ── INIT ──
document.addEventListener('DOMContentLoaded', () => {
  applyLang();
  initAnim();
  initNav();
  initFAQ();
  initForm();
  initCursor();
  addHaptics();
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
});
